// 定义宏的文件：macros.rs
// 注意：文件名通常为 macros.rs
#[macro_use]
mod macros {
    macro_rules! my_macro {
        () => {
            println!("Check out my macro!");
        };
    }
}

fn main() {
    my_macro!();
}

